<h2>هدف  المشروع</h2>
<p>
هدف مشروع آيسبل هو إنشاء قواميس في اللغة العربية للتطبيقات المكتبية الحرة مثل مجموعة أوبن أوفس OpenOffice.org، متصفح فابرفوكس Firefox، مراسل تاندربيرد Thunderbird، أبيوورد Abiword، جيديت Gedit ...الخ. يخضع هذا المشروع للرخص الثلاثية GPL/LGPL/MPL.
</p>
; فيما يتعلق بالجانب القاموسي للمشروع، نعمل جاهدين على توفير:
<ul>
  <li>القاموس العربي للتدقيق الإملائي: <a href="doc/hunspell-ar.pdf">hunspell-ar</a> (توثيق عربية/pdf).&nbsp; يعمل هذا القاموس ضمن برنامج&nbsp; هانسل <a  target="_blank" href="http://hunspell.sourceforge.net/">Hunspell</a> المدقق الإملائي المرتبط بمشروع أوبن أوفس ؛</li>
  <li>القاموس العربي للمترادفات : &nbsp;thesaurus-ar&nbsp;، المرتبط ببرنامج الترادف المعجمي ميت <a  href="http://lingucomponent.openoffice.org/MyThes-1.zip">MyThe</a> لمشروع أوبن أوفس ؛&nbsp;<a href="http://ayaspell.sourceforge.net/syno/">المترادفات على الويب</a></li>
  <li>القاموس العربي للمفرداتالمصنفة لغويا : grammar-ar&nbsp;، الخاص بالتدقيق النحوي ؛&nbsp;</li>
  <li>القاموس العربي الخفيف للتدقيق الإملائي&nbsp;للأنظمة المحمولة<a  href="http://en.wikipedia.org/wiki/Embedded_system" target="_blank"> Embedded systems</a> كالهواتف المحمولة و أجهزة PDA..</li>
</ul>

